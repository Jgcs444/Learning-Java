package curso_programacao_1;

public class Casting {

	public static void main(String[] args) {
		
		// Comando de atribuição, conversão explicita de valores
		
		//Ex:
		
		int[] valores = {5,2};
		
		int a = valores [0];
		int b = valores [1];
		
		double area = (double) a / b;
		
		System.out.println(area);

	}

}
